/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var threeSum = function (nums) {
  nums.sort();

  let n = nums.length;

  const buckets = nums.reduce((acc, val, index) => {
    if (!acc.has(val)) {
      acc.set(val, []);
    }

    acc.get(val).push(index);

    return acc;
  }, new Map());

  const result = [];
  const resultSet = new Set();

  for (let i = 0; i < n; i++) {
    if (i < n - 1 && nums[i] === nums[i + 1]) {
      continue;
    }

    const num1 = nums[i];

    for (let j = 0; j < n; j++) {
      if (i === j) {
        continue;
      }

      const num2 = nums[j];
      const num3 = -(num1 + num2);

      const num3Indexes = buckets.get(num3);

      if (num3Indexes?.find((x) => x !== i && x !== j) !== undefined) {
        const triple = [num1, num2, num3];
        triple.sort();

        const hash =
          triple[0] * 1e5 + triple[1] * 1e5 * 1e5 + triple[2] * 1e5 * 1e5 * 1e5;

        if (!resultSet.has(hash)) {
          result.push(triple);
          resultSet.add(hash);
        }
      }
    }
  }

  return result;
};

